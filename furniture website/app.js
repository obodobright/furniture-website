const showMenu = () => {
    document.getElementById('nav-list').style.display = "block"
    document.getElementById('toggle-icon').style.display = "none"
    document.getElementById('navbar-brand').style.display = "none"

}
const hideMenu = () => {
    document.getElementById('nav-list').style.display = "none"
    document.getElementById('toggle-icon').style.display = "block"
    document.getElementById('navbar-brand').style.display = "block"
}


